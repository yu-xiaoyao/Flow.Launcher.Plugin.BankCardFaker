        /// <summary>
        /// 计算银行卡校验码 (Luhn算法/模10算法)
        /// 从右边第1个数字开始每隔一位乘以2，把乘积各位数字相加，再与未乘2的各位数字相加，
        /// 对求和值个位数求10的补数，个位数为0则校验码为0。
        /// </summary>
        private static int GetVerificationCode(string bankcard)
        {
            var digits = bankcard.ToCharArray();
            Array.Reverse(digits);

            int evenSum = 0;
            int oddSum = 0;

            for (int i = 0; i < digits.Length; i++)
            {
                int digit = digits[i] - '0';
                if (i % 2 == 1)
                {
                    evenSum += digit;
                }
                else
                {
                    int doubled = digit * 2;
                    oddSum += doubled / 10 + doubled % 10;
                }
            }

            int total = oddSum + evenSum;
            int mod = total % 10;
            return mod == 0 ? 0 : 10 - mod;
        }

        /// <summary>
        /// 生成银行卡号
        /// </summary>
        /// <param name="bankInfo"></param>
        /// <returns>1: 银行卡号. 2: 银行名称. 3: 卡类型</returns>
        public static string BuildCardNum(BankCardInfo bankInfo)
        {
            var fillLength = bankInfo.CardLength - bankInfo.Bin.Length - 1;

            var cardNo = bankInfo.Bin;
            for (var j = 0; j < fillLength; j++)
            {
                cardNo += _random.Next(10);
            }

            cardNo += GetVerificationCode(cardNo);
            return cardNo;
        }

        /// <summary>
        /// 生成指定数量的随机银行卡号
        /// </summary>
        /// <param name="count">生成银行卡个数</param>
        /// <returns>银行卡号和银行名称(卡类型)的列表</returns>
        public static List<(string CardNo, string BankInfo)> Build(int count)
        {
            var result = new List<(string, string)>(count);
            for (var i = 0; i < count; i++)
            {
                var bankInfo = BankConfig[_random.Next(BankConfig.Length)];
                var fillLength = bankInfo.CardLength - bankInfo.Bin.Length - 1;

                var cardNo = bankInfo.Bin;
                for (var j = 0; j < fillLength; j++)
                {
                    cardNo += _random.Next(10);
                }

                cardNo += GetVerificationCode(cardNo);

                result.Add((cardNo, bankInfo.BankName + "(" + bankInfo.CardType + ")"));
            }

            return result;
        }
